package com.newspace.payplatform.syncgameinfo.vo;

import java.util.Date;

import com.newspace.common.vo.BaseVo;

/**
 * SyncGameVo.java 
 * 描 述:  同步游戏信息Vo类  
 * 作 者:  liushuai
 * 历 史： 2014-9-16 创建
 */
public class SyncGameVo extends BaseVo
{
	private static final long serialVersionUID = 1L;

	private String appId;

	private String gameId;

	private String cpId;

	private String gameName;

	private String packageName;

	private Date gameCreateTime;

	private Date createTime;

	@Override
	public String logString()
	{
		StringBuilder sb = new StringBuilder();
		sb.append("{ gameId: ").append(gameId);
		sb.append("{,gameName: ").append(gameName);
		sb.append("{,cpId: ").append(cpId).append(" }");
		return sb.toString();
	}

	public String getAppId()
	{
		return appId;
	}

	public void setAppId(String appId)
	{
		this.appId = appId;
	}

	public String getGameId()
	{
		return gameId;
	}

	public void setGameId(String gameId)
	{
		this.gameId = gameId;
	}

	public String getCpId()
	{
		return cpId;
	}

	public void setCpId(String cpId)
	{
		this.cpId = cpId;
	}

	public String getGameName()
	{
		return gameName;
	}

	public void setGameName(String gameName)
	{
		this.gameName = gameName;
	}

	public String getPackageName()
	{
		return packageName;
	}

	public void setPackageName(String packageName)
	{
		this.packageName = packageName;
	}

	public Date getGameCreateTime()
	{
		return gameCreateTime;
	}

	public void setGameCreateTime(Date gameCreateTime)
	{
		this.gameCreateTime = gameCreateTime;
	}

	public Date getCreateTime()
	{
		return createTime;
	}

	public void setCreateTime(Date createTime)
	{
		this.createTime = createTime;
	}
}
